package Nested_Loops;

public class example {
    public static void main(String[] args) {
        // Nested Loop = A loop inside another loop often used with matrices or DSA
        for(int i = 1;i<=3;i++){
            for(int j = 1;j<=10;j++){
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
