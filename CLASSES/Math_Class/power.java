package Math_Class;

public class power {
    public static void main(String[] args) {
        System.out.println("\"To raise a power to a number we use Pow from Math class\"");
        double result;
        result = Math.pow(2,3);
        System.out.println(result);
        System.out.println("\"For absolute value we have abs\"");
        System.out.println(Math.abs(-5));
        System.out.println("\"For square root we have sqrt\"");
        System.out.println(Math.sqrt(9));
        System.out.println("\"To round a number we have round\"");
        System.out.println(Math.round(3.14));
        System.out.println("To round up we have ceil");
        System.out.println(Math.ceil(3.14));
        System.out.println("To round down we have floor");
        System.out.println(Math.floor(5.8));
        System.out.println("For maximum number we have max");
        System.out.println(Math.max(20,10));
        System.out.println("For minimum number we have min");
        System.out.println(Math.min(20, 10));
    }    
}
